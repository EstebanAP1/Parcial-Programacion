    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        conexion=new Conexion();
        Connection cn=conexion.getConexion();
        String nombre, apellido, tel, ciu, id;
        String sql="";
        
        nombre=txtNombre.getText();
        apellido=txtApellido.getText();
        tel=txtTel.getText();
        ciu=txtCiudad.getText();
        id=txtId.getText();
        
        sql="UPDATE usuario SET nombre=?, apellido=?, telefono=?, ciudad=? WHERE id=?";
        try{
            PreparedStatement psd = (PreparedStatement) cn.prepareStatement(sql);
            psd.setString(1, nombre);
            psd.setString(2, apellido);
            psd.setString(3, tel);
            psd.setString(4, ciu);
            psd.setInt(5, Integer.parseInt(id));
            
            int a=psd.executeUpdate();
            if(a>0){
                JOptionPane.showMessageDialog(null,"Registro modificado");
                limpiar();
                bloquear();
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Error "+ex);
            limpiar();
        }
        
        
    }//GEN-LAST:event_btnEditActionPerformed

