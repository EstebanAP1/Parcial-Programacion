    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        conexion=new Conexion();
        Connection cn=conexion.getConexion();
        String usuario;
        String sql="";
        
       usuario=txtNombre.getText();  
        sql="INSERT INTO usuario (usuario) VALUES (?)";
        try{
            PreparedStatement psd = (PreparedStatement) cn.prepareStatement(sql);
            psd.setString(1, usuario);
            int a=psd.executeUpdate();
            if(a>0){
                JOptionPane.showMessageDialog(null,"Registro guardado");
                limpiar();
                bloquear();
                mostrar();
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Error "+ex);
            limpiar();
        }
        
        
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed

        conexion=new Conexion();
        Connection cn=conexion.getConexion();
        String sql="", id;
        
        id=txtId.getText();
        
        sql="SELECT * FROM usuario WHERE id=?";
        
        try{
            PreparedStatement psd = (PreparedStatement) cn.prepareStatement(sql);
            psd.setInt(1, Integer.parseInt(id));
            
            ResultSet a=psd.executeQuery();
            if(a.next()){
                JOptionPane.showMessageDialog(null,"Registro encontrado");
                txtNombre.setText(a.getString("nombre"));
                txtApellido.setText(a.getString("apellido"));
                txtTel.setText(a.getString("telefono"));
                txtCiudad.setText(a.getString("ciudad"));
                
                
            }else{
                JOptionPane.showMessageDialog(null,"Registro no encontrado");
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Error "+ex);            
        }
        
        
        
    }//GEN-LAST:event_btnFindActionPerformed

